fun main() {
    //numero pari, resto 0
    println(42 % 2)
//numero dispari, resto 1
    println(41 % 2)
//numero divisibile per 5, resto 0
    println(100 % 5)
//numero non divisibile per 5
//resto diverso da zero
    println(103 % 5)
//numero divisibile per 7, resto 0
    println(49 % 7)
//numero non divisibile per 7
// resto diverso da zero
    println(44 % 7)
}