fun main() {
//maggiore di >
    println(5 > 4)
    println(1 > 1)
    println("zucca" > "albero")
    println("Zucca" > "albero")
//maggiore uguale di >=
    println(10 >= 1)
    println(1 >= 1)
    println((3 + 1) >= (2 + 2))
//minore di <
    println(8 < 2)
//minore uguale di <=
    println(7 <= 7)
    println("zucca" <= "albero")

//uguaglianza
    println(1 == 1)
    println((3 + 1) == (2 + 2))
    println(1 == 10)
//disuguaglianza
    println(1 != 1)
    println(1 != 10)
    println("Foofology" != "foofology")
}